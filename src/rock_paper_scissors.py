import random                                                             # Импортируем random для случайного выбора хода компьютера

print("Игра: Камень, ножницы, бумага")                                    # Приветствие
print("Чтобы выйти, введи: выход\n")                                      # Инструкция по завершению игры

choices = ["камень", "ножницы", "бумага"]                                 # Возможные варианты выбора для игрока и компьютера

while True:                                                               # Основной цикл, выполняется бесконечно, пока пользователь не введёт "выход"
    user = input("Твой выбор (камень/ножницы/бумага): ").lower()          # Получаем выбор игрока
    
    if user == "выход":
        print("Игра окончена. Пока!")                                     # Сообщаем об окончании игры
        break
    
    if user not in choices:
        print("Неверный ввод. Попробуй снова.")                           # Проверка корректность ввода
        continue

    computer = random.choice(choices)                                     # Компьютер делает случайный выбор из списка
    print("Компьютер выбрал:", computer)                                  # Показываем выбор компьютера

    if user == computer:
        print("Ничья!\n")                                                 # Одинаковые ходы — ничья
    elif (user == "камень" and computer == "ножницы") or \
         (user == "ножницы" and computer == "бумага") or \
         (user == "бумага" and computer == "камень"):
        print("Ты победил!\n")                                            # Победа пользователя по правилам игры
    else:
        print("Ты проиграл!\n")                                           # В остальных случаях — победа компьютера

